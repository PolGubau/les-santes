/**
 * compute-blurhashes.cjs
 *
 * Reads every event mock + IMAGE_BY_TYPE, downloads each unique image,
 * computes its blurhash with sharp + blurhash, caches results on disk, and
 * emits `src/entities/event/blurhashes.generated.ts` exporting
 *   export const BLURHASH_BY_URL: Record<string, string>
 *
 * Run with:
 *   node app/scripts/compute-blurhashes.cjs
 */
const fs = require("node:fs");
const path = require("node:path");
const sharp = require("sharp");
const { encode } = require("blurhash");

const ROOT = path.resolve(__dirname, "..");
const SOURCES = [
	path.join(ROOT, "src/entities/event/mock-2025.ts"),
	path.join(ROOT, "src/entities/event/mock-2026.ts"),
];
const OUT_TS = path.join(ROOT, "src/entities/event/blurhashes.generated.ts");
const CACHE_DIR = path.join(__dirname, ".cache");
const CACHE_JSON = path.join(CACHE_DIR, "blurhashes.json");

fs.mkdirSync(CACHE_DIR, { recursive: true });

// ── 1. Harvest every `"https://..."` string that looks like an image URL ────
const urls = new Set();
const urlRe = /"(https:\/\/[^"\s]+\.(?:jpe?g|png|webp|avif|gif)(?:\?[^"]*)?)"/gi;
for (const file of SOURCES) {
	if (!fs.existsSync(file)) continue;
	const src = fs.readFileSync(file, "utf8");
	let m;
	while ((m = urlRe.exec(src)) !== null) urls.add(m[1]);
}
console.log(`Found ${urls.size} unique image URLs across ${SOURCES.length} files`);

// ── 2. Load existing cache so we skip already-computed URLs ─────────────────
const cache = fs.existsSync(CACHE_JSON)
	? JSON.parse(fs.readFileSync(CACHE_JSON, "utf8"))
	: {};

// ── 3. Compute blurhashes for missing URLs ──────────────────────────────────
async function computeBlurhash(url) {
	const res = await fetch(url);
	if (!res.ok) throw new Error(`HTTP ${res.status} ${res.statusText}`);
	const buf = Buffer.from(await res.arrayBuffer());

	// Downscale to keep blurhash encoding fast and deterministic.
	const { data, info } = await sharp(buf)
		.raw()
		.ensureAlpha()
		.resize(32, 32, { fit: "inside" })
		.toBuffer({ resolveWithObject: true });

	// componentX / componentY: 4×3 keeps hashes short (~30 chars) and faithful.
	return encode(new Uint8ClampedArray(data), info.width, info.height, 4, 3);
}

async function main() {
	const pending = [...urls].filter((u) => !cache[u]);
	console.log(`Cached: ${urls.size - pending.length} · Pending: ${pending.length}`);

	let done = 0;
	const CONCURRENCY = 6;
	const queue = [...pending];

	async function worker(workerId) {
		while (queue.length > 0) {
			const url = queue.shift();
			if (!url) return;
			try {
				const hash = await computeBlurhash(url);
				cache[url] = hash;
				done++;
				console.log(`  [${done}/${pending.length}] (w${workerId}) ${hash}  ${shortUrl(url)}`);
			} catch (err) {
				console.warn(`  [skip] ${shortUrl(url)} → ${err.message}`);
			}
		}
	}

	await Promise.all(
		Array.from({ length: Math.min(CONCURRENCY, queue.length) }, (_, i) => worker(i + 1)),
	);

	// Persist cache (sorted for clean diffs).
	const sorted = Object.fromEntries(
		Object.entries(cache).sort(([a], [b]) => a.localeCompare(b)),
	);
	fs.writeFileSync(CACHE_JSON, JSON.stringify(sorted, null, 2));

	// ── 4. Emit blurhashes.generated.ts ─────────────────────────────────────
	const header = [
		"/**",
		" * AUTO-GENERATED by app/scripts/compute-blurhashes.cjs — DO NOT EDIT.",
		" *",
		" * Maps each event image URL to its precomputed blurhash. Regenerate by",
		" * running `node app/scripts/compute-blurhashes.cjs` from the repo root.",
		" */",
		"",
		"export const BLURHASH_BY_URL: Record<string, string> = {",
	];
	const rows = Object.entries(sorted).map(
		([url, hash]) => `\t${JSON.stringify(url)}: ${JSON.stringify(hash)},`,
	);
	const footer = ["};", ""];
	fs.writeFileSync(OUT_TS, [...header, ...rows, ...footer].join("\n"));

	console.log(`\nWrote ${OUT_TS} (${Object.keys(sorted).length} entries)`);
}

function shortUrl(u) {
	return u.length > 70 ? `${u.slice(0, 35)}…${u.slice(-30)}` : u;
}

main().catch((err) => {
	console.error(err);
	process.exit(1);
});
